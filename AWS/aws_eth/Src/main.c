void HAL_Delay( uint32_t ulDelayMs )
{
    if( xTaskGetSchedulerState() != taskSCHEDULER_NOT_STARTED )
    {
        vTaskDelay( pdMS_TO_TICKS( ulDelayMs ) );
    }
    else
    {
        uint32_t ulStartTick = HAL_GetTick();
        uint32_t ulTicksWaited = ulDelayMs;

        /* Add a freq to guarantee minimum wait */
        if( ulTicksWaited < HAL_MAX_DELAY )
        {
            ulTicksWaited += ( uint32_t ) ( HAL_GetTickFreq() );
        }

        while( ( HAL_GetTick() - ulStartTick ) < ulTicksWaited )
        {
            __NOP();
        }
    }
}


static BaseType_t prvComputeEthMac( uint8_t mac[6] )
{
  BaseType_t status = pdFALSE;
  uint8_t cpuID[3 * sizeof(uint32_t)] = { 0 };

  *( (uint32_t*) &cpuID[0] ) = HAL_GetUIDw0();
  *( (uint32_t*) &cpuID[4] ) = HAL_GetUIDw1();
  *( (uint32_t*) &cpuID[8] ) = HAL_GetUIDw2();

  uint32_t sum = Fletcher16( (uint16_t *)cpuID, sizeof(cpuID) / sizeof(uint16_t) );
  mac[0] = 0x02;              /* Prefix denoting a locally administered range. */
  mac[1] = 0x00;
  memcpy(&mac[2], &sum, 4);   /* Append the 32b checksum. */
  status = pdTRUE;

  return status;
}


static uint32_t Fletcher16( uint16_t *data, int words )
{
  /* Non-optimized implementation with 16b accumulator */
  uint32_t sum1 = 0;
  uint32_t sum2 = 0;
  int index;
  for ( index = 0; index < words; ++index )
  {
    sum1 = (sum1 + data[index]) % 0xffff;
	  sum2 = (sum2 + sum1) % 0xffff;
  }
  return (sum2 << 16) | sum1;
}


void low_level_init(NetInterface_t *netif)
{
  uint32_t PHYLinkState = LAN8742_STATUS_LINK_DOWN;
  uint32_t speed = 0, duplex =0;
  ETH_MACConfigTypeDef MACConf = { 0 };

  /* set MAC hardware address length */
  netif->hwaddr_len = ETH_HWADDR_LEN;

  /* set MAC hardware address */
  netif->hwaddr[0] =  heth.Init.MACAddr[0];
  netif->hwaddr[1] =  heth.Init.MACAddr[1];
  netif->hwaddr[2] =  heth.Init.MACAddr[2];
  netif->hwaddr[3] =  heth.Init.MACAddr[3];
  netif->hwaddr[4] =  heth.Init.MACAddr[4];
  netif->hwaddr[5] =  heth.Init.MACAddr[5];

  /* maximum transfer unit */
  netif->mtu = ETH_MAX_PAYLOAD;

  /* Accept broadcast address and ARP traffic */
  /* don't set NETIF_FLAG_ETHARP if this device is not an ethernet one */
  #if LWIP_ARP
    netif->flags |= NETIF_FLAG_BROADCAST | NETIF_FLAG_ETHARP;
  #else
    netif->flags |= NETIF_FLAG_BROADCAST;
  #endif /* LWIP_ARP */

  /* Set PHY IO functions */
  LAN8742_RegisterBusIO(&LAN8742, &LAN8742_IOCtx);

  /* Initialize the LAN8742 ETH PHY */
  LAN8742_Init(&LAN8742);

  PHYLinkState = LAN8742_GetLinkState(&LAN8742);

  /* Get link state */
  if(PHYLinkState <= LAN8742_STATUS_LINK_DOWN)
  {
    netif_set_link_down(netif);
    netif_set_down(netif);
  }
  else
  {
    switch (PHYLinkState)
    {
      case LAN8742_STATUS_100MBITS_FULLDUPLEX:
        duplex = ETH_FULLDUPLEX_MODE;
        speed = ETH_SPEED_100M;
        break;
      case LAN8742_STATUS_100MBITS_HALFDUPLEX:
        duplex = ETH_HALFDUPLEX_MODE;
        speed = ETH_SPEED_100M;
        break;
      case LAN8742_STATUS_10MBITS_FULLDUPLEX:
        duplex = ETH_FULLDUPLEX_MODE;
        speed = ETH_SPEED_10M;
        break;
      case LAN8742_STATUS_10MBITS_HALFDUPLEX:
        duplex = ETH_HALFDUPLEX_MODE;
        speed = ETH_SPEED_10M;
        break;
      default:
        duplex = ETH_FULLDUPLEX_MODE;
        speed = ETH_SPEED_100M;
        break;
    }

    /* Get MAC Config MAC */
    HAL_ETH_GetMACConfig(&heth, &MACConf);
    MACConf.DuplexMode = duplex;
    MACConf.Speed = speed;
    HAL_ETH_SetMACConfig(&heth, &MACConf);
  }
}

